    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            
            List<Nodo> recorrido = arbol.recorridoPreOrden();
            
            // Crear la cadena para el JLabel
            StringBuilder labelText = new StringBuilder("Recorrido Preorden:\n");
            for (Nodo nodo : recorrido) {
                Libro libro = nodo.contenido; // Suponiendo que el contenido es un Libro
                labelText.append(nodo.llave).append(" - ").append(libro.getNombre()).append(" - ").append(libro.getAnio()).append("\n");
            }
            
            jTextArea1.setText(labelText.toString());
        
        } catch(Exception ex){
        
            jTextArea1.setText(ex.getMessage());
        
        }
    }//GEN-LAST:event_jButton4ActionPerformed

