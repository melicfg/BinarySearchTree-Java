    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        try {
        
            int id = (int) jSpinner4.getValue();
            
            Nodo libro = arbol.buscar(id);
            
            if(libro != null) {
                Libro libroContenido = libro.contenido;

                 String multiLineText = String.format(
                        "<html>ID: %d<br>Nombre: %s<br>A AÃ±o: %d</html>", libroContenido.getId(), libroContenido.getNombre(), libroContenido.getAnio());
                 jLabel12.setText(multiLineText);
            } else {
                jLabel12.setText("El libro no fue encontrado");
            }
        } catch(Exception ex) {
         jLabel12.setText(ex.getMessage());
        }
    }//GEN-LAST:event_jButton10ActionPerformed

